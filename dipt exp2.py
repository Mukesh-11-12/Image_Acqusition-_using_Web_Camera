import cv2
import matplotlib.pyplot as plt
from IPython.display import clear_output
import time
cap = cv2.VideoCapture(0)
ret, frame = cap.read()
if ret:
    cv2.imwrite("weed.jpeg", frame)
cap.release()
captured_image = cv2.imread('weed.jpeg')
plt.imshow(captured_image[:,:,::-1])
plt.axis('off')
plt.show()


